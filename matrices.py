# Takes nothing
# Concatenates the identity matrix I and the B' matrix horizontally
# Only calculates the matrix once
# Returns the generator matrix G
G = None
def get_G():
    global G
    if G is None:
        G = concatenate_matrices(I, B_, axis=1)
    return G

# Takes nothing
# Concatenates the identity matrix I and the B matrix vertically
# Only calculates the matrix once
# Returns the parity check matrix H
H = None
def get_H():
    global H
    if H is None:
        H = concatenate_matrices(I, B, axis=0)
    return H
